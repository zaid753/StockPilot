
import { GoogleGenAI } from '@google/genai';

let ai: GoogleGenAI | null = null;
export const getAi = () => {
    if (!ai) {
        if (!process.env.API_KEY) {
            throw new Error("API_KEY environment variable not set");
        }
        ai = new GoogleGenAI({ apiKey: process.env.API_KEY });
    }
    return ai;
};
